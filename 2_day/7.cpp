#include<iostream>
using namespace std;

int main()
{
    // Switch case and functions
    int num =2;
    switch(num)
    {
        case 1: cout<<"First"<<endl;
        break;
        case 2: cout<<"Second"<<endl;
        break;
        case 3: cout<<"Third"<<endl;
        break;
        case 4: cout<<"Four"<<endl;
        break;
        default:
        cout<<"Not a number"<<endl;

    }
    return 0;
}